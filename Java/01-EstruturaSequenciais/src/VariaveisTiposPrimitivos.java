
/*
Inteiros: byte, short, int, long
Pontos Flutuente: float, double
Caractere: char (um único caractere Unicode)
Booleano: boolean (Representa verdadeiro ou falso)

 */
public class VariaveisTiposPrimitivos {
    static void main(String[] args) {
        int idade = 30;
        long quantidadeCachorros = 10L;
        double salarioMensal = 4500.00;
        char genero = 'M';
        boolean empregadoJava = true;

        System.out.println("idade: " + idade);
        System.out.println("Salario: " + salarioMensal);
        System.out.println("Genero: " + genero);
        System.out.println("Empregado Java: " + empregadoJava);
        System.out.println("Quantidade: " + quantidadeCachorros);
    }
}
